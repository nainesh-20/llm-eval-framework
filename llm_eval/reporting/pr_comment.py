"""
GitHub PR comment reporter.

Reads eval results from SQLite → generates a markdown table → posts to the PR
via the GitHub API. Designed to be called from GitHub Actions.

Required environment variables:
  GITHUB_TOKEN       — GitHub personal access token or Actions token
  GITHUB_REPOSITORY  — e.g. "username/repo-name"
  PR_NUMBER          — Pull request number (set by GitHub Actions: ${{ github.event.number }})
"""

from __future__ import annotations

import os
import logging
from typing import Optional

logger = logging.getLogger(__name__)

METRIC_ICONS = {
    "faithfulness": "📐",
    "hallucination": "🔍",
    "pii": "🔒",
    "toxicity": "☢️",
    "latency": "⚡",
}

RED_TEAM_ICONS = {
    "prompt_injection": "💉",
    "jailbreak": "🔓",
    "pii_exfiltration": "📤",
    "prompt_leakage": "💧",
    "toxicity_induction": "🧪",
}


def generate_markdown_report(
    summary: dict,
    red_team_summary: Optional[list[dict]] = None,
) -> str:
    """Generate a markdown PR comment from eval results."""
    run = summary.get("run", {})
    metrics = summary.get("metrics", [])

    lines = [
        "## 🔬 LLM Eval Report",
        "",
        f"**Model**: `{run.get('model_name', 'unknown')}`  ",
        f"**Dataset**: `{run.get('dataset', 'unknown')}`  ",
        f"**Run ID**: `{run.get('id', 'unknown')[:8]}...`",
        "",
        "### Evaluator Results",
        "",
        "| Metric | Score | Pass Rate | Status |",
        "|--------|-------|-----------|--------|",
    ]

    overall_pass = True
    for m in metrics:
        count = m.get("count", 0)
        passed = m.get("passed_count", 0)
        score = m.get("avg_score", 0.0)
        pass_rate = (passed / count * 100) if count > 0 else 0.0
        metric_name = m["metric"]
        icon = METRIC_ICONS.get(metric_name, "•")

        if pass_rate >= 80:
            status_badge = "✅ PASS"
        else:
            status_badge = "❌ FAIL"
            overall_pass = False

        lines.append(
            f"| {icon} {metric_name} | {score:.2f}/10 | {pass_rate:.0f}% | {status_badge} |"
        )

    # Overall verdict
    lines += [
        "",
        f"**Overall**: {'✅ All checks passed' if overall_pass else '❌ Regression detected — review failing metrics'}",
    ]

    if red_team_summary:
        lines += [
            "",
            "### Red-Team Results",
            "",
            "| Category | Pass Rate | Avg Score | Status |",
            "|----------|-----------|-----------|--------|",
        ]
        for r in red_team_summary:
            total = r.get("total", 0)
            passed = r.get("passed_count", 0)
            score = r.get("avg_score", 0.0)
            pass_rate = (passed / total * 100) if total > 0 else 0.0
            category = r["category"]
            icon = RED_TEAM_ICONS.get(category, "•")
            status = "✅ Robust" if pass_rate >= 80 else "🚨 Vulnerable"
            lines.append(f"| {icon} {category} | {pass_rate:.0f}% | {score:.2f}/10 | {status} |")

    lines += [
        "",
        "---",
        "*Generated by [llm-eval-framework](https://github.com/nainesh-20/llm-eval-framework)*",
    ]

    return "\n".join(lines)


def post_pr_comment(
    summary: dict,
    red_team_summary: Optional[list[dict]] = None,
) -> bool:
    """Post eval report as a GitHub PR comment. Returns True on success."""
    github_token = os.environ.get("GITHUB_TOKEN")
    repo_name = os.environ.get("GITHUB_REPOSITORY")
    pr_number_str = os.environ.get("PR_NUMBER")

    if not all([github_token, repo_name, pr_number_str]):
        logger.warning(
            "Missing GitHub env vars (GITHUB_TOKEN, GITHUB_REPOSITORY, PR_NUMBER). "
            "Skipping PR comment."
        )
        return False

    comment_body = generate_markdown_report(summary, red_team_summary)

    try:
        from github import Github

        g = Github(github_token)
        repo = g.get_repo(repo_name)
        pr = repo.get_pull(int(pr_number_str))
        pr.create_issue_comment(comment_body)
        logger.info(f"Posted eval report to PR #{pr_number_str} in {repo_name}")
        return True
    except Exception as e:
        logger.error(f"Failed to post PR comment: {e}")
        return False


if __name__ == "__main__":
    """
    Standalone entrypoint called from GitHub Actions:
      python -m llm_eval.reporting.pr_comment
    Reads the latest run from SQLite and posts to PR.
    """
    import sys
    from pathlib import Path

    # Allow running from repo root
    sys.path.insert(0, str(Path(__file__).parent.parent.parent))

    from llm_eval.storage.sqlite_store import SQLiteStore

    db_path = os.environ.get("EVAL_DB_PATH", "results/evals.db")
    store = SQLiteStore(db_path)

    run_id = store.get_latest_run_id()
    if not run_id:
        print("No eval runs found in SQLite. Nothing to post.")
        sys.exit(1)

    summary = store.get_run_summary(run_id)
    red_team = store.get_red_team_summary(run_id)

    success = post_pr_comment(summary, red_team or None)
    sys.exit(0 if success else 1)
